func TestRoundTrips(t *testing.T) {
	for _, theTest := range tests {
		ebcdicDataOut, err := FromUnicode([]rune(theTest.unicodeData), theTest.codePage)

		assert.Equalf(t, theTest.err, err, "FromUnicode(%s, %d) \nExpected Err: %v; \nGot Err: %v", theTest.unicodeData, theTest.codePage, theTest.err, err)
		assert.Equalf(t, theTest.ebcdicData, ebcdicDataOut, "FromUnicode(%s, %d) \nExpected Output: %v; \nActual Output: %v", theTest.unicodeData, theTest.codePage, theTest.ebcdicData, ebcdicDataOut)

		unicodeDataOut, err := ToUnicode(theTest.ebcdicData, theTest.codePage)

		//assert.Equalf(t, theTest.err, err, "ToUnicode(%s, %d) \nExpected Err: %v; \nGot Err: %v", theTest.ebcdicData, theTest.codePage, theTest.err, err)
		//assert.Equalf(t, theTest.unicodeData, string(unicodeDataOut), "ToUnicode(%s, %d) \nExpected Output: %v; \nActual Output: %v", theTest.unicodeData, theTest.codePage, theTest.unicodeData, string(unicodeDataOut))

		ebcdicDataOutOut, err := FromUnicode(unicodeDataOut, theTest.codePage)

		assert.Equalf(t, theTest.err, err, "FromUnicode(%s, %d) \nExpected Err: %v; \nGot Err: %v", unicodeDataOut, theTest.codePage, theTest.err, err)
		assert.Equalf(t, theTest.ebcdicData, ebcdicDataOutOut, "FromUnicode(%s, %d) \nExpected Output: %v; \nActual Output: %v", theTest.unicodeData, theTest.codePage, theTest.ebcdicData, ebcdicDataOutOut)

		unicodeDataOutOut, err := ToUnicode(ebcdicDataOut, theTest.codePage)

		assert.Equalf(t, theTest.err, err, "ToUnicode(%v, %d) \nExpected Err: %v; \nGot Err: %v", ebcdicDataOut, theTest.codePage, theTest.err, err)
		assert.Equalf(t, string(unicodeDataOutOut), string(unicodeDataOut), "ToUnicode(%v, %d) \nExpected Output: %v; \nActual Output: %v", theTest.ebcdicData, theTest.codePage, string(unicodeDataOutOut), string(unicodeDataOut))
	}
}
